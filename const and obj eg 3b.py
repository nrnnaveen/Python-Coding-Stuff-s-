class calculator :
    def add(self,a,b):
        print("Answer is:",a+b)
    def sub(self,a,b):
        print("answer is :",a-b)
    def mul(self,a,b):
        print("Answer is:",a*b)
    def div(self,a,b):
        print("Answer is :",a/b)
cal=calculator()
cal.add(40,80)
cal.sub(40,80)
cal.mul(40,80)
cal.div(40,80)
